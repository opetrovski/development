package org.oscm.internal.types.enumtypes;

/**
 * Specifies the types of sessions the platform records information for.
 */
public enum SessionType {

    /**
     * The session is related to a service provided by the platform.
     */
    SERVICE_SESSION,

    /**
     * The session is related to the platform.
     */
    PLATFORM_SESSION;

}
